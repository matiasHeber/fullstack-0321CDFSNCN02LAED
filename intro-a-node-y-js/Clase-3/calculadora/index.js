let calculadora = {
    multiplicar: function (num1 , num2) {
        return num1 * num2;
    },

    dividir: function (num1 , num2) {
        return num1 / num2;
    },

    sumar: function (num1, num2) {
        return num1 + num2;
    },

    restar: function (num1, num2) {
        return num1 - num2;
    },
}

module.exports = calculadora;